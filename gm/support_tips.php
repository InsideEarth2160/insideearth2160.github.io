        <TD WIDTH=459 valign="top" bgcolor="#010102" style="background-image:url(images/index_06.gif); background-repeat:no-repeat;" ALT=""><p align="left" style="margin-left:10px; margin-right:10px;"><strong class="h1"> SPIELTIPS</strong></p>
          <p align="justify"><br>
              <span class="Stil1">Q:</span> Mit welcher Kampagne soll ich starten? <br>
            <span class="Stil1">A:</span> Es empfiehlt sich mit der ED-Kampagne zu starten, da diese über umfangreiches Tutorial verfügt und Ihnen das Grundwissen zur Kameraführung und zur Einheitensteuerung näher erläutert. </p>
          <p align="justify"><span class="Stil1">Q:</span> Über manchen Gebäuden blinkt ein gelbes Blitz-Symbol. Was ist da los? <br>
            <span class="Stil1">A:</span> Da fehlt der Saft. Ohne genügend Strom geht nichts. Entweder das Kraftwerk ist außer Reichweite (UCS), dann müssen Sie eine Transmitter bauen, oder Sie brauchen ein zusätzliches Kraftwerk (LC,ED,UCS). </p>
          <p align="justify"><span class="Stil1">Q:</span> Ich will, dass meine Truppen vor der Basis patroulieren. Wie mache ich das? <br>
            <span class="Stil1">A:</span> Selektieren Sie die Einheiten, drücken "R" für Aufzeichnung, klicken auf die Wegpunkte, die abgefahren werden sollen und beenden die Aufzeichnung mit "X" für Ausführung. Leider gehen nur maximal 65.512 Wegpunkte ;-) </p>
          <p align="justify"><span class="Stil1">Q:</span> Die Alien-Raumschiffe rauben mir den letzten Nerv. Wie kann ich die vom Himmel holen? <br>
            <span class="Stil1">A:</span> Es ist wichtig, dass Sie frühzeitig Luftabwehr-Raketen erforschen. Den Aliens kann man mit Energiewaffen wenig anhaben. </p>
          <p align="justify"><span class="Stil1">Q:</span> Ich hab meinen Helden in ein Gebäude geschickt und finde Ihn nicht mehr. Wo ist er? <br>
            <span class="Stil1">A:</span> Klicken Sie auf das Helden-Pictogramm links auf dem Bildschirm. Damit selektieren Sie den Gebäudeteil, in dem er sich befindet. Nun müssen Sie nur noch den Befehl zum Gebäude verlassen erteilen. </p>
          <p align="justify"><span class="Stil1">Q:</span> Mir liegt die Maussteuerung nicht. Ich bin gewohnt mit der Rechten Maustaste Befehle zu erteilen. Geht das? <br>
            <span class="Stil1">A:</span> Sie können eine alternative Maussteuerung unter Einstellungen Steuerung festlegen. Dann funktioniert alles so, wie Sie es gewohnt sind. </p>
          <p align="justify"><span class="Stil1">Q:</span> Die ganze 3D Dreherei ist mir zu viel, ich verliere die Übersicht. Kann man das abstellen? <br>
            <span class="Stil1">A:</span> Bitte wählen Sie unter Einstellungen - Interface (Steuerung) - Fixe Kamera. Eine weitere Einstellung "Autozoom" könnte Sie auch Ihrer gewohnten Spielumgebung näher bringen. </p>
          <p align="justify"><span class="Stil1">Q:</span> Ich stehe im Nebel, der ganze Bildschirm flimmert schwarz und weiß. Wie beseitige ich diese Bildstörung? <br>
            <span class="Stil1">A:</span> Das ist der Fog-of-War, der unerforschtes Gebiet verschleiert. Klicken Sie in aufgedecktes Gebiet auf der Minimap. Dann sehen Sie wieder klar. </p>
          <p align="justify"><span class="Stil1">Q:</span> (ED) Ich will ein Tor bauen, aber meine Mauer steht schon. Wie mache ich das? <br>
            <span class="Stil1">A:</span> Verkaufen Sie die Mauerstücke, die Sie ersetzen wollen. </p>
          <p align="justify"><span class="Stil1">Q:</span> (ED/UCS) Meine Sammler sind vernichtet, ich habe keine Ressourcen mehr, wie geht es weiter? <br>
            <span class="Stil1">A:</span> Um Ressourcen für einen neuen Sammler zu erhalten, ohne sie abzubauen, können Sie ein oder mehrere Gebäude verkaufen. Aber stornieren Sie laufende Forschungs- und Bauaufträge, sonst sind die Ressourcen gleich wieder weg. </p>
          <p align="justify"><span class="Stil1">Q:</span> Im Multiplayer auf dem EarthNet Server machen mich alle platt. Schon nach ein paar Sekunden werde ich angegriffen und verliere. Wie kann ich das Spiel besser lernen? <br>
            <span class="Stil1">A:</span> Sie sollten zunächst die Kampagnen gespielt haben um die Forschungen und den Basisbau der unterschiedlichen Fraktionen kennen zu lernen. Im weiteren empfiehlt sich im Skirmisch gegen Computergegner zu üben, bevor man sich auf den Server wagt. </p>
          <p align="justify"><span class="Stil1">Q:</span> Wie macht man Screenshots? <br>
            <span class="Stil1">A:</span> Einfach während des Spiels die Druck-Taste betätigen. Die Screens werden im BMP Format im Ordner Eigene Dateien im Unterverzeichnis Earth 2160 Files abgelegt. Es empfiehlt sich zunächst mit der "Pause"-Taste das Spiel anzuhalten, dann die Kameraposition festzulegen und den Shot zu machen. Wenn eine Einheit selektiert ist, können Sie mit der Tab-Taste jeweils 3 Bilder weiter springen. </p>
          <p align="justify"><span class="Stil1">Q:</span> Bei mir friert alle 5 Minuten der Bildschirm für ein paar Sekunden ein. Ist das normal? <br>
            <span class="Stil1">A:</span> Das passiert durch die Auto-Speichern-Funktion. Die ist auf 5 Minuten voreingestellt. Unter Einstellungen, können Sie diese sowohl auf längere Intervalle einstellen oder auch völlig ausschalten. </p>
          <p align="justify"><span class="Stil1">Q:</span> Earth 2160 läuft auf meinem PC schrecklich langsam, oder ich muss die Grafikeinstellungen sehr stark einschränken. Wie kann ich meinen PC am besten aufrüsten? <br>
            <span class="Stil1">A:</span> Die Earth 2160 Engine, führt nahezu alle Grafikberechnungen auf der Grafikkarte aus. Die Geschwindigkeit des Prozessors spielt kaum eine Rolle.<br>
  Um den PC Earth2160-fit zu machen ersetzen Sie am besten Ihre Grafikkarte. Ein gutes Preis-Leistungsverhältnis haben die ATI Radeon X700 Modelle. Wenn Sie natürlich tief in den Geldbeutel greifen wollen, wird Ihnen Earth 2160 jegliche High-Endkarte mit hohen Frame-Raten danken. </p>
          <p align="justify"><span class="Stil1">Q:</span> Ich habe das Spiel auf 2 PCs installiert, aber Multiplayer zwischen den beiden PCs funktioniert nicht. Warum? <br>
            <span class="Stil1">A:</span> Für Multiplayer-Spiele muss jede Installation eine verschiedene Seriennummer haben. Sie brauchen ein weiteres Spiel, bzw. eine weitere Seriennummer. <br>
          </p>

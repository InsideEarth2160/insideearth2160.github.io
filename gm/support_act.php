          <p align="justify"><u><strong>FRAGEN ZUR PRODUKTAKTIVIERUNG</strong></u> <br>
              <span class="Stil1">Q:</span> Wie kann ich Earth 2160 noch spielen, wenn in einigen Jahren der Support möglicherweise eingestellt wird und die Aktivierung nicht mehr möglich ist?<br>
            <span class="Stil1">A:</span> Sollte die Aktivierung aus technischen oder anderen Gründen in einigen Jahren nicht mehr möglich sein, so wird von uns rechtzeitig ein entsprechender Patch zur Verfügung gestellt.</p>
          <p align="justify"><span class="Stil1">Q:</span> Welche Daten werden bei der Online Aktivierung von meinem PC übertragen?<br>
            <span class="Stil1">A:</span> Es wird nur eine 16-stellige nur aus Zahlen bestehende Aktivierungsnummer übertragen und ein 16-stelliger Freischaltcode zurückgesendet. Diese Nummer enthält Teile der Seriennummer.</p>
          <p align="justify"><span class="Stil1">Q:</span> Kann ich mir Earth 2160 in der Videothek ausleihen?<br>
              <span class="Stil1">A:</span> Nein. Grundsätzlich ist der kommerzielle Verleih der Verkaufsversionen unserer Spiele nicht gestattet. Wenn Sie Earth 2160 mieten wollen, so steht dieser Dienst ab August bei T-Online Games On Demand zur Verfügung.</p>
          <p align="justify"><span class="Stil1">Q:</span> Wie oft kann man, oder muss man mit einer Version auf verschiedenen Rechnern (z.B. Notebook und Stand-Alone-PC) aktivieren?<br>
              <span class="Stil1">A:</span> Ohne Hardware-Wechsel beliebig oft. Bei einem Hardware-Wechsel 3. Mal online oder per Telefon ohne Registrierung. Das 4. Mal erfordert eine Registrierung. Für weitere Aktivierungen, die grundsätzlich möglich sind, ist ein persönlicher Kontakt zu Zuxxez erforderlich. Solange es sich um den gleichen Benutzer oder den Käufer einer gebrauchten Version handelt, wird eine Freischaltung erteilt.</p>
          <p align="justify"><span class="Stil1">Q:</span> Was passiert, wenn man den Titel verkaufen will. Kann sich der Käufer mit derselben Version wieder neu registrieren oder diese aktivieren?<br>
              <span class="Stil1">A:</span> Ja. Wenn es schon 4 Hardwarewechsel gegeben hat, muss der neue Kunde bei der Hotline anrufen und sagen, dass er es gebraucht gekauft hat. Dann wird die Aktivierung zurückgesetzt und es kann neu aktiviert werden. In diesem Fall empfiehlt es sich einen Kaufnachweis aufzubewahren.<br>
              <br>
              <span class="Stil1">Q:</span> Muss das Spiel bei jeder neuen Installation neu aktiviert werden oder wo kann ich den in der Bestätigungsmail angegeben Code erneut eingeben?<br>
              <span class="Stil1">A:</span> Man kann das Spiel bei gleicher Hardware-Konfiguration beliebig oft aktivieren. Oder man kann den Code den man schon hat (weil der sich ja nicht ändert) noch mal eingeben. Dazu einfach die Schaltfläche "telefonische Aktivierung" wählen, jedoch nicht anrufen, sondern den Code eingeben den man schon hat.<br>
              <br>
              <span class="Stil1">Q:</span> Wie oft kann man Earth 2160 pro Tag aktivieren?<br>
              <span class="Stil1">A:</span> So oft man will - Wenn die Hardware gleich bleibt.<br>
              <br>
              <span class="Stil1">Q:</span> Ich installiere öfter mal mein Betriebsystem neu. Muss ich dann immer aktivieren?<br>
              <span class="Stil1">A:</span> Ja, nach jeder Neuinstallation müssen Sie erneut aktivieren. Das geht aber auch mit dem alten Freischaltcode, wenn sich die Hardware nicht verändert hat. Natürlich können Sie auch einfach jedes Mal über Internet neu aktivieren. Das ist bequemer als den Code zu suchen und einzugeben.</p>
          <p align="justify"><span class="Stil1">Q:</span> Muss ich bei der Telefonaktivierung persönliche Daten angeben und wenn ja welche?<br>
              <span class="Stil1">A:</span> Nein. Nur wenn wegen 3-maligem Hardwarewechsel die Aktivierung nicht mehr geht, muss man sich registrieren lassen.</p>
          <p align="justify"><span class="Stil1">Q:</span> Braucht jeder Spieler beim Multiplayer eine eigene Vollversion mir eigenem Code?<br>
              <span class="Stil1">A:</span> Ja. Jede Version die gleichzeitig in einem MP Spiel ist muss eine unterschiedliche Seriennummer haben. </p>
          <p align="justify"><span class="Stil1">Q:</span> Was passiert, wenn ich das Spiel meinem Freund ausleihe und der diese Version aktiviert?<br>
            <span class="Stil1">A:</span> Dann liegt die aktive Version zunächst mal auf dem PC Ihres Freundes. Wenn Sie später auf Ihrem eigenen PC neu aktivieren wollen, rufen Sie am Besten die Hotline an und erklären den Vorfall.</p>
          <p align="justify"><span class="Stil1">Q:</span> Darf ich Earth 2160 zusätzlich auf meinem Notebook installieren und auch aktivieren?<br>
              <span class="Stil1">A:</span> Solange Sie nicht beide Spiele gleichzeitig benutzen betrachten wird das als zulässig.</p>
          <p align="justify"><span class="Stil1">Q:</span> Darf ich von Earth 2160 eine Sicherheitskopie anfertigen?<br>
              <span class="Stil1">A:</span> Ja. Solange Sie diese Sicherheitskopie nicht an alle Freunde und Verwandte verteilen, sondern es wirklich eine Sicherheitskopie ist, kann das nicht schaden.</p>

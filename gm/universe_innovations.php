        <TD WIDTH=459 valign="top" bgcolor="#010102" style="background-image:url(images/index_06.gif); background-repeat:no-repeat;" ALT=""><p align="justify" style="margin-left:10px; margin-right:10px;"><strong class="h1"> Innovationen </strong><em><br>
                  <br>
            </em><strong>Virtuelle Agenten: </strong><br>
            <br>
            Der erstmalige Einsatz von virtuellen Agenten ist der Beginn einer völlig neuen Ära im EARTH Universum. Die Agenten können als Söldner angeworben werden, agieren selbständig und unterstützen den Spieler.<br>
            <br>
Jeder Agent hat verschiedene Fähigkeiten. Manche unterstützen die Forschungen oder die Steuerung der Einheiten, andere erhöhen die Effizienz der Rohstoffgewinnung. Wieder andere spionieren Gegner aus oder stehlen die Technologie der Gegenseite.<br>
    <br>
Virtuelle Agenten werden bei "Söldnerauktionen" angeworben, wobei mehrere Spieler den Agenten ihr Angebot unterbreiten. Das eröffnet viele interessante Möglichkeiten. Man kann z.B. einen Gegner in den finanziellen Ruin stürzen, indem man den Preis für einen begehrten Söldner in die Höhe treibt.<br>
    <br>
Aber Achtung! Die virtuellen Agenten haben ein gutes Gedächtnis. Sie erinnern sich an frühere Verträge mit dem Spieler und die gemeinsame Vorgeschichte hat einen großen Einfluss auf den Sold. Ein Spieler sollte sich gut um seine VA's kümmern, sonst haben sie später keine Lust mehr, für ihn zu arbeiten.<br>
    <br>
Es ist den VA's auch nicht egal, mit welchen anderen Söldnern sie zu tun haben. Manche können sich leiden, andere nicht. Diese Beziehungen zu anderen Söldnern können bis hin zu Hassgefühlen und spezieller Zuneigung gehen. Es ist also durchaus möglich, dass ein VA für ziemlich wenig Kohle arbeitet, nur weil der Gegner seinen meistgehassten VA engagiert hat!<br>
    <br>
Nicht alle VA's sind jederzeit verfügbar. Wenn ein Söldner bei einem Spieler unter Vertrag steht, kann er nicht von einem anderen Spieler angeheuert werden. Wie im echten Leben. Aber Vorsicht, denn wie im echten Leben können die Virtuellen Agenten auch die Seiten wechseln.<br>
    <br>
    <strong>Modularer Aufbau: <br>
    <br>
    </strong> Die meisten Einheiten und Gebäude sind modular angelegt. Der Spieler kann seine Einheiten aus einer riesigen Bandbreite von Einzelmodulen zusammensetzen. Keine Armee wird der anderen gleichen. Jeder Spieler wird in die Lage versetzt, seine eigene, einzigartige Armee zu schaffen. <br>
    <br>
Auch die Basen werden aus vielen Modulen zusammengesetzt. Auf diese Weise sind sie sehr flexibel verbaubar. Der Spieler kann jederzeit eine Basis errichten, welche zu einem bestimmten Gelände oder zu einer bestimmten Aufgabe passt. Wenn man zum Beispiel einen LC Wolkenkratzer errichtet, hat man die freie Wahl, welches Segment auf welchem Stockwerk untergebracht wird. Dies ermöglicht zahllose unterschiedliche Kombinationen.<br>
    <br>
    <strong>Freie Forschung:</strong><br>
    <br>
    Ein Spieler kann an so vielen Technologien forschen, wie er Lust hat, wobei die Reihenfolge nicht vorgegeben ist. Der Forschungsdrang wird nur durch die vorhandenen Ressourcen eingegrenzt (Anzahl der Forschungszentren, Geld, Energie). Manche Zweige eines Forschungsbaumes bleiben zunächst verschlossen, bis eine andere Technologie zugänglich wird. Das "missing link" kann z.B. ein Artefakt der Aliens sein, welches man von einem Verbündeten erwerben oder von einem Gegner stehlen kann. </p>

          <p align="justify"><u><strong>FRAGEN ZU HARDWAREPROBLEMEN</strong></u> <br>
              <span class="Stil1">Q:</span> Earth 2160 l&auml;uft auf meiner GeForce FX5xxx nur ruckelig. Was kann ich tun ?<br>
            <span class="Stil1">A:</span> Bei Grafikkarten der nVidia Geforce FX5xxx Serie ist das Shader Modell 2.0 sehr langsam. Earth 2160 kann auf diesen Karten auch mit Shader 1.3 ausgeführt werden. Einfach den <a href="UseShaderVersion1.3.zip" target="_blank"><strong>bereitgestellten Regkey downloaden</strong></a>, entpacken und ausf&uuml;hren. Dann sollte Earth 2160 besser laufen.  </p>
          <p align="justify"><span class="Stil1">Q:</span> Ich habe ein 


 K7S5A

Mainboard und Earth 2160 crasht kurz nach dem Start auf den Desktop zur&uuml;ck?<br>
            <span class="Stil1">A:</span> Dieses Problem kann mit einem Biosupdate gel&ouml;st werden. Eine Anleitung dazu finden Sie <a href="http://www.biosflash.com/bios-update-anleitung.htm" target="_blank"><strong>hier</strong></a>. Die ben&ouml;tigten Dateien 


 (das 03/08/11 update [030811.zip] und das amiflash programm [

 
 aminf341.zip]) findet Ihr <a href="http://www.lejabeach.com/ECS/ez.html" target="_blank"><strong>hier</strong></a> in der obersten Zeile der Tabelle.<br>
 <strong>INFO:</strong> Bei wem es mit dem obrigem Bios nicht geklappt hat, kann es auch mit <a href="K7S5A900.rar"><strong>diesem BIOS</strong></a> versuchen<strong>!</strong><br>
 Laut Useraussagen hat dieses BIOS dann letztendlich die Probleme behobenen.</p>
          <p align="justify">              <strong>ACHTUNG:</strong> <strong>Ein falsch ausgef&uuml;hrtes Biosupdate kann das Mainboard besch&auml;digen!</strong></p>
          <p align="justify"><strong><span class="Stil2">Q:</span> </strong>Ich sitze hinter einem Router und / oder einer Firewall und habe Verbindungsprobleme beim Multiplayer. Was kann ich tun? <strong><br>
              <span class="Stil2">A:</span> </strong>Wenn Sie hinter einem Router sitzen und / oder eine Firewall Ihre Verbindungen überwacht, dann m&uuml;ssen Sie ein paar Vorkehrungen treffen, um auch über Internet Earth 2160 ohne Probleme spielen zu können. <br>
 <br>
 Earth 2160 benötigt zumindest einen UDP-Port, unter dem Ihr Rechner erreichbar ist. Im Prinzip können Sie sich einen aussuchen, solange er sich nicht mit anderen Leuten hinter demselben Router überschneidet, aber wenn nichts dagegen spricht, würden wir den Port 17771 empfehlen. Diesen Port geben Sie in Ihrer Firewall frei (für eingehende und ausgehende Verbindungen!), oder, wenn Sie eine Firewall haben, die ihre Regeln nach Anwendungen setzt, dann erlaubt ihr Earth 2160, sich mit dem Internet zu verbinden. Wenn Sie die Windows XP Firewall verwenden, dann trägt sich Earth 2160 bei der Installation automatisch in deren Ausnahmeliste ein. <br>
 Bei Ihrem Router sollten Sie den gewählten Port nun auf Ihren eigenen Rechner weiterleiten (Optionen hierfür meistens unter Namen wie 'Forward' oder DNAT). Geben Sie den Port ein sowie die IP Ihres Rechners im lokalen Netzwerk. Wenn Sie ein Protokoll auswählen könnt, dann wählt UDP bzw. Datagram. <br>
 <br>
 Schließlich starten Sie Earth 2160, öffnen Sie die Einstellungen, und unter der Seite Netzwerk aktivieren Sie das Häkchen vor "Custom Port". Tragen Sie dann den von Ihnen gewählten Port ein, 17771 z. B.. <br>
 Wenn Sie einen Router haben, ist es darüber hinaus zum Hosten meistens noch nötig, das Häkchen vor "NAT Resolver" ebenfalls zu setzen. Die Einstellungen im Textfeld daneben lasst ihr auf ihrem Standardwert. <br>
 <br>
 
 Mit diesen Vorkehrungen müssten Sie nun für Multiplayerschlachten im EarthNet gerüstet sein. Am besten probieren Sie dort mal, ein Spiel zu hosten und bitten jemanden, Ihrem Spiel beizutreten. Wenn dies funktioniert, ist alles in bester Ordnung. <strong><br>
 <br>
 
 <strong>Troubleshooting: </strong> </strong></p>
          <ul>
            <li>
              <div align="justify">Wenn trotz dem geschilderten Vorgehen niemand Ihren Spielen beitreten kann, dann probieren Sie zunächst, den "NAT Resolver" einzuschalten, wenn Sie dies noch nicht getan haben. Falls es dann immer noch nicht geht, müssen Sie möglicherweise den Port 17172 UDP in Ihrer Firewall freigeben oder diesen Port vom Router sogar auf Ihren Rechner weiterleiten. </div>
            </li>
            <li>
              <div align="justify">Es gibt momentan noch ein paar Probleme in Hinsicht auf das Beitreten zu einem Spiel. Wenn Sie also im Allgemeinen beitreten können, es bei manchen Spielen aber einfach nicht klappen will, dann liegt dieser Fehler nicht bei Ihnen. Wichtig hierbei ist, dass alle anderen Teilnehmer des Spiels ihren Router und ihre Firewall auch gemäß dieser Anleitung korrekt eingestellt haben.</div>
            </li>
          </ul>

</em>EARTH 2160 ist das offizielle Sequel der preisgekrönten und beliebten EARTH Serie und schreibt die Geschichte der Topseller Earth 2140 und <a href="http://www.earth2150.com" target="_blank">Earth 2150</a> fort.<br>
              <br>
Was zuvor geschah: Im Jahr 2150 n. Chr. gelingt einigen tausend Menschen die Flucht vom blauen Planeten. kurz vor dem Untergang der Erde. Die nächsten Jahre verbringen die Überlebenden damit, den roten Planeten zu erschließen und neue Technologien zu entwickeln. Doch man muss nicht glauben, dass die verfeindeten Fraktionen "Eurasian Dynasty", "Lunar Corporation" und "United Civilised States" in der Fremde zusammen halten. Vielmehr arbeitet jede Fraktion für sich. <br>
Bis im Jahr 2160 der entscheidende Wendepunkt eintritt. Nun ist alles möglich und es liegt in Hand des Spielers, ob die drei Fraktionen weiter gegeneinander kämpfen oder ob sie ihre Kräfte bündeln. <br>
              <br>
EARTH 2160 bietet spektakuläre Schauplätze auf unterschiedlichen Planeten und Trabanten. Manche Schlachtfelder sind besonders seltsam, es handelt sich um Planeten mit reflektierenden flüssigen Oberflächen, welche in ihren Bewegungen absolut natürlich wirken. Die Flüssigkeiten (Wasser, Quecksilber u.a.) sind je nach Wetterlage leicht gekräuselt, haben starken Seegang und können verschiedene Temperaturen haben.


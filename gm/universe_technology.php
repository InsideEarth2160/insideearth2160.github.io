        <TD WIDTH=459 valign="top" bgcolor="#010102" style="background-image:url(images/index_06.gif); background-repeat:no-repeat;" ALT=""><p align="justify" style="margin-left:10px; margin-right:10px;"><strong class="h1"> Technologie </strong><em><br>
                  <br>
            </em>Das Spiel verwendet die aktuelle Earth<strong> -</strong> 4 Engine.<br>

            <br>
Diese Engine wurde für Earth 2160 durchgängig neu geschrieben. Sie ist äußerst leistungsfähig und erlaubt eine nie gesehene Detailgenauigkeit, welche eigentlich nur durch die verwendete Hardware eingeschränkt wird. 
Ein eigenes T&L Rendering Modul unterstützt in aktuellen Grafikkarten "per Pixel shader" und "Bone calculation". Die Shader Technologie ermöglicht die Erzeugung von verschiedenen reflektierenden Oberflächen, dynamische und farbige Lichteffekte sowie absolut echt wirkende Flüssigkeiten, welche ihr Aussehen verändern, je nach Wetter und anderen Einflüssen.
<br>
    <br>
Eine vollkommen neue Technologie ermöglicht erstaunlich echt wirkende Materialien. Metall, Glas, staubbedeckte Oberflächen, Stoffe, Haut, . alle diese Materialien erscheinen "fühlbar" echt. Keine spärlich kaschierten Polygone, wie in vielen gängigen 3D Anwendungen. Die Spielwelt erhält durch die Verwendung dieser neuen Materialien eine unvergleichliche Atmosphäre.<br>
    <br>
Zusätzliche "Bump maps" und "Specula maps" legen weitere unregelmäßige Muster um Objekte und tragen zum immensen Detailreichtum und dem "reality effect" der Spielwelt bei, indem sie Geometrie der Objekte noch komplexer machen.<br>
    <br>
Darüber hinaus verändern die Materialien dynamisch ihr Aussehen, vorzugsweise um absolut realistische Spuren der Zerstörung zu darzustellen: Löcher, Brandspuren, Kratzer, Risse und was das Herz sonst begehrt.<br>
    <br>
Von der Hardware unterstütze Partikelsysteme ermöglichen eine spektakuläre Pyrotechnik (Explosionen, Rauch, Staubwolken) und Lichteffekte .<br>

    <br>
    Das KI-System in Earth 2160 ist absolut einzigartig: Auf der Grundlage von "back framing Algorithmen" handeln die Computergegner intelligent und selbstlernend. Der Spieler wird in eine neue Dimension des RTS Genres hineingezogen.<br>
    <br>
Intelligente Wegfindungs-Funktionen und Bewegungs-Routinen sorgen für vollkommen natürlich wirkende Einheiten.<br>
    <br>
    Vom gigantischen Raumschiff bis zum Starfighter, alle Luftfahrzeuge können mit fantastisch weichen Flugbahnen aufwarten. Die Earth community kann sich auf spektakuläre Luftkämpfe freuen...  </p>

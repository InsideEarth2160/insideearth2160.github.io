          <p align="justify"><u><strong>FRAGEN ZUR INSTALLATION</strong></u> <br>
            <span class="Stil1">Q:</span> Beim Starten der Installation erscheint eine Meldung: ...Setup.exe ist keine gültige Win32- Anwendung&quot;. Was ist zu tun? <br>
            <span class="Stil1">A:</span> Wird diese Fehlermeldung ausgegeben, sollten Sie alle im Hintergrund aktiven Programme beenden. Sie erhalten Einsicht in eine Liste der momentan laufenden Programme indem Sie gleichzeitig die Tastenkombination STRG-ALT-ENTF drücken. Danach schließen Sie jedes einzelne Programm, außer SYSTRAY und Explorer. Starten Sie dien Installation erneut. </p>
          <p align="justify"><span class="Stil1">Q:</span> Bei der Installation von Earth 2160 treten Probleme auf. Die Installation wird nicht ordnungsgemäß durchgeführt. <br>
              <span class="Stil1">A:</span> Haben Sie ein Virenerkennungsprogramm installiert, sollten Sie dieses vor der Installation deaktivieren. Aktive Virenscanner können den Ablauf einer Installation negativ beeinträchtigen. </p>          <p align="justify"><span class="Stil1">Q:</span> Das Installationsprogramm stoppt immer wieder bei Erreichen einer bestimmten Prozentzahl. Wieso? <br>
                <span class="Stil1">A:</span> Es gibt drei mögliche Gründe: <br>
                <strong>1.</strong> Sie haben nicht genug Speicherplatz auf Ihrer Festplatte. Bitte löschen Sie überflüssige Programme, um mehr Platz frei zu machen, und installieren Sie Earth 2160 erneut. <br>
                <strong>2.</strong> Dateien werden auf einen beschädigten Bereich Ihrer Festplatte geschrieben. Wenn das der Fall ist, sollten Sie Scandisk laufen lassen, und zwar mit der Option Intensiv. Nachdem das Programm seine Arbeit erledigt und die Platte für fehlerfrei erklärt hat, installieren Sie Earth 2160 noch einmal. <br>
                <strong>3.</strong> Vielleicht ist die DVD selber durch Flecken oder Fingerabdrücke verschmutzt. Schauen Siesich die DVD an - ist sie schmutzig, reinigen Sie sie mit einem weichen, sauberen und fusselfreien Tuch, indem Sie von der Mitte der DVD gerade nach außen wischen. <br>
          </p>
          <p align="justify"><u><strong>FRAGEN ZUM SPIELSTART</strong></u> <br>
            <span class="Stil1">Q:</span> Beim Starten von Earth 2160 unter Windows 2000 wird die DVD-ROM nicht erkannt.Anwendung&quot;. Was ist zu tun? <br>
            <span class="Stil1">A:</span> Windows 2000 User benötigen Administratorrechte, um das Spiel richtig installieren und spielen zu können. </p>
          <p align="justify"><span class="Stil1">Q:</span> Beim Spielstart erscheint eine Fehlermeldung "Ein Fehler ist bei der Initialisierung von Direct3D aufgetreten. Das k&ouml;nnte an der Grafikkarten-Konfiguration oder am Treiber liegen. Bitte versuchen Sie das Programm im Save-Mode zu starten (-safemode execution parameter)."<br>
            <img src="images/4mx.jpg" width="436" height="109" hspace="15" vspace="5">              <span class="Stil1">A:</span> Ihre Grafikkarte unterst&uuml;tzt keinen


 Per-Pixel Shader 1.2 oder h&ouml;her! Sie ben&ouml;tigen leider eine neue Grafikkarte.   </p>
          <p align="justify"><span class="Stil1">Q:</span> Beim Spielstart erscheint eine Fehlermeldung "Die Datei d3d9.dll kann nicht gefunden werden". <br>
            <span class="Stil1">A:</span> Installieren Sie die DirectX, Version 9.0c, und starten Sie den Computer neu. Nach diesem Vorgang sollte das Spiel funktionieren. </p>
          <p align="justify"><span class="Stil1">Q:</span> Wenn ich Earth 2160 starte, bekomme ich diese Fehlermeldung: &quot;Die Anwendung Earth2160_SSE.exe (bzw. Earth2160_No_SSE.exe) bezieht sich auf Speicherplatz bei Adresse xxxx:xxxx, der nicht gelesen werden kann.&quot; <br>
            <span class="Stil1">A:</span> Wahrscheinlich ist Ihr installierter Grafikkartentreiber nicht kompatibel zu DirectX. Die einzige Lösung ist, sich einen DirectX 9.0c - kompatiblen Treiber vom Hersteller Ihrer Grafikkarte zu besorgen. </p>
          <p align="justify"><span class="Stil1">Q:</span> Wenn ich Earth 2160 starte, verschwindet mein Mauszeiger und mein Rechner hängt sich auf. <br>
            <span class="Stil1">A:</span> Wahrscheinlich ist Ihr installierter Soundkartentreiber nicht kompatibel zu DirectX. Die einzige Lösung ist, sich einen DirectX 9.0c-kompatiblen Treiber vom Hersteller Ihrer Soundkarte zu besorgen.<br>
            <br>
            <span class="Stil1">Q:</span> Im Spiel sind keine Musik und keine Soundeffekte zu hören, obwohl die Hardware richtig angeschlossen ist. <br>
            <span class="Stil1">A:</span> Stellen Sie sicher, das Ihre Soundkarte kompatibel zu DirectX 9.0c oder höher ist. Zudem sollten Sie sich mit dem Hersteller Ihrer Soundkarte in Verbindung setzen, um die aktuellsten Treiber zu erhalten.<br>
            <br>
            <span class="Stil1">Q:</span> Die Grafik wird verzerrt dargestellt. <br>
            <span class="Stil1">A:</span> Sie benutzen wahrscheinlich benutzen einen veralteten Grafikkartentreiber. Setzen Sie sich mit dem Hersteller Ihrer Graphikkarte in Verbindung, um einen aktuelleren Treiber zu erhalten.<br>
            <br>
            <span class="Stil1">Q:</span> Earth 2160 bricht ohne Fehlermeldung ab und kehrt zurück zum Desktop. <br>
            <span class="Stil1">A:</span> Dieses Problem kann mehrere Ursachen haben. Hier eine Liste der häufigsten Übeltäter: <br>
  Vergewissern Sie sich, dass die DVD-Rom sauber ist (überprüfen Sie die Leseoberfläche dem Datenträger auf Flecken und Kratzer). <br>
  Vergewissern Sie sich, dass das Spiel richtig installiert worden ist. <br>
  Vergewissern Sie sich, dass DirectX 9.0c richtig installiert worden ist. <br>
  Vergewissern Sie sich, dass Sie die aktuellsten Treiber für Ihre Grafikkarte haben und dass Sie DirectX 9.0c kompatibel sind.<br>
  Vergewissern Sie sich, dass Sie die aktuellsten Treiber für Ihre Soundkarte haben und dass sie DirectX 9.0c kompatibel sind. <br>
  Vergewissern Sie sich, dass die Option Virtual Memory auf Ihrem Rechner aktiviert ist. <br>
  Lassen Sie Scandisk laufen und lassen Sie die Defragmentierung laufen.<br>
  Löschen Sie alte temporäre Dateien (.TMP) aus dem Verzeichnis C:\WINDOWS\TEMP oder C:\WINx\TEMP auf Ihrer Festplatte (vom Windows Explorer aus) oder führen Sie eine Datenträgerbereinigung durch. <br>
  Vergewissern Sie sich, dass Sie keine Anti-Virus-Programme wie Norton's AntiVirus im Hintergrund laufen haben, wenn Sie Earth 2160 starten. <br>
  Vergewissern Sie sich, dass Sie keine Windows-Diskcachingprogramme im Hintergrund laufen haben, wenn Sie Earth 2160 starten. <br>
  Versuchen Sie es mit einer Deinstallation des Spiels und anschließender Neuinstallation. <br>
  Verlassen Sie das Spiel, booten Sie Ihren Rechner neu und versuchen Sie es erneut. <br>
  Bitte beachten Sie: wenn Ihr Rechner zum Desktop zurückkehrt, während Sie Earth 2160 spielen, sollten Sie auf jeden Fall den Rechner ganz neu starten, bevor Sie versuchen, weiterzuspielen. Sonst können DirectDraw oder DirectSound möglicherweise "hängen", und das Spiel kann dadurch Ihre Sound- und Grafikhardware nicht mehr ansprechen. </p>
          <p align="justify"><span class="Stil1">Q:</span> Beim Spielstart erhalte ich die Fehlermeldung: "Keine iinstallierte 3D Grafikkarte gefunden" bzw. "Keine D3D9 Unterstützung gefunden. Was ist zu tun? <br>
            <span class="Stil1">A:</span> Auch in diesem Fall gilt: Sie benutzen nicht den aktuellsten Grafikkartentreiber und / oder nicht die aktuellsten DirectX 9.0 Version. Bitte aktualisieren Sie den Treiber und installieren Sie anschließend DirectX mindestens in der Version 9.0c. </p>
          <p align="justify"><span class="Stil1">Q:</span> Wenn ich Earth 2160 starte, sehe ich das Earth Logo und danach lande ich wieder auf dem Windows Desktop. Wie kann ich Earth starten? <br>
            <span class="Stil1">A1:</span> Dieses Problem tritt dann auf, wenn Sie eine ältere Grafikkarte z.B. eine Geforce 3 TI in Ihrem Rechner haben. Um das Spiel trotzdem zu starten, gehen Sie bitte wie folgt vor: <br>
  Klicken Sie auf Start -Ausführen und geben Sie Regedit ein. <br>
  Sichern Sie zunächst die Windows Registry, indem Sie auf Datei -Exportieren klicken. Wählen Sie einen Speicherort und vergeben Sie einen Dateinamen. Klicken Sie auf den Button Alles unter dem Dateinamen und wählen Sie anschließend "Speichern". <br>
  Wechseln Sie nun in den Zweig: <br>
  HKEY_CURRENT_USER\Software\Reality Pump\Earth2160\Graphics <br>
  Doppelklicken Sie auf den Wert MultisampleType und geben Sie anstatt der 2 eine 0 ein. <br>
  Verlassen Sie die Windows Registry und starten Sie das Spiel erneut. <br>
  Alternativ dazu können Sie Earth 2160 im geschützten Modus starten indem Sie die entsprechende Verknüpfung im Startmenü in der Earth 2160 Programmgruppe aufrufen. <br>
  <span class="Stil1">A2:</span> Dieses Problem kann auch an 


 der Data Execution Prevention (DEP), die mit SP2 installiert wurde, liegen. 
 Hier die L&ouml;sungen dazu: <br>
 <br>
 <strong>Lösungsvorschlag 1: </strong><br>
Voraussetzung: Service Pack 2 installiert!!<br>
<br>
<strong>Variante a) </strong>Deaktivieren der "Datenausführungsverhinderung" in der boot.ini <br>

Klickt: Sart - Einstellung - Systemsteuerung - System - Erweitert - Starten und Wiederherstellen: Einstellungen - Systemstart: Bearbeiten <br>

Die letze Zeile in folgende ändern <br>
multi(0)disk(0)rdisk(0)partition(1)\WINDOWS="Microsoft Windows XP Home Edition" /fastdetect <strong>/execute </strong><br>

Speichern, Neustart - Fertig. <br>

<br>
<strong>Variante b) </strong>"Datenausführungsverhinderung" für alle Programme aktiv lassen, bis auf die Earth.exe Dateien <br>

Wie in Variante a) die Boot.ini öffnen, und nachsehen, ob die letzte Zeile diesmal folgendes enthält: <br>

multi(0)disk(0)rdisk(0)partition(1)\WINDOWS="Microsoft Windows XP Home Edition" /fastdetect <strong>/noexecute=OptOut </strong><br>

Falls nicht: reinschreiben, speichern, OK drücken. <br>
Im vorigen Fenster nun auf "Systemleistung: Einstellungen - Datenausführungsverhinderung" klicken. <br>

Dort die Variante: "Datenausführungsverhinderung für alle Programme und Dienste mit Ausnahme der ausgewählten aktivierten" wählen und über "hinzufügen" alle 6 Earth*.exe Dateien aus dem Spielverzeichnis hinzufügen. <br>

Speichern - Neustart - Fertig. <br>
<br>
<strong>Lösungsvorschlag 2: </strong><br>
Falls der 1. Lösungsvorschlag nicht klappt, kann man versuchen Earth 2160 im Fenster zu starten.<br>
Dies ist zwar nicht die komfortabelste Lösung, aber man kann dann immerhin spielen, bis die Entwickler einen entsprechenden Patch rausgebracht haben: <br>

Starte das Spiel im Safe-Mode. Wähle dann unter Einstellungen eine Auflösung (bspw. 800x600) und markiere den Fenster Modus! <br>

Beende den Safe-Mode und starte dann das Spiel normal. Earth 2160 sollte nun im Fenster starten mit den vorher im Safe-Mode eingegebenen Detaileinstellungen. <br>
<span class="Stil1">A3:</span> Eine weitere Ursache f&uuml;r dieses Problem kann die Installation sein. Es kann vorkommen das die Installationsroutine nicht die vollst&auml;ndigen Dateien kopiert. Sollte sich bei einem Vergleich der installierten Dateien mit den Dateien der DVD herausstellen das manche installierte Dateien kleiner sind wie die entsprechenden auf der DVD, kopieren Sie diese einfach manuel von der DVD in das entsprechende Verzeichnis Ihrer Installation und &uuml;berschreiben die unvollst&auml;ndigen Dateien.  </p>
          <p align="justify"><br>
              <u><strong>MULTIPROZESSORSYSTEME UND HYPERTHREADING PROZESSOREN </strong></u><br>
              <span class="Stil1">Q:</span> Auf Systemen mit mehreren Prozessoren oder auf Systemen bei denen mehrere virtuelle Prozessoren simuliert werden, kann es vorkommen, dass der Ton in Cutscenes asynchron zum Bild läuft, oder das die Sprachausgabe der Helden vorzeitig abgebrochen wird.<br>
              <span class="Stil1">A:</span> Dieses Problem wird dadurch verursacht, dass die Spiel-Engine kein konstantes Timingsignal vom Prozessor erhält, wenn noch andere Programme im Hintergrund laufen.<br>
              Um die Synchronisation dennoch zu gewährleisten können Sie Earth 2160 mit folgenden Parametern starten: <br>
              Earth2160_SSE.EXE -cpu_UsePerformanceCounter <br>
              Dadurch wird die Synchronisation errechnet und kein CPU Timingsignal mehr benutzt <br>
              Earth2160_SSE.EXE -cpu_UseTickCount <br>
              Das Timingsignal der Wechselfrequenzstromquelle wird zur Synchronisation benutz. <br>
              Earth2160_SSE.EXE -cpu_UseOneProcessor <br>
              Das Programm wird nur auf einem Prozessor ausgeführt. Achtung, falls Ihre Prozessoren unter 2 GHz getaktet sind kann dies zu Performanzverlusten führen. <br>
          </p>
          <p align="justify"><u><strong>TIPPS ZU GRAFIKPROBLEMEN</strong></u> <br>
            <span class="Stil1">Q:</span> Sollte Ihre Grafikkarte zu heiß werden und daraufhin der Rechner mittem im Spiel neu booten, empfiehlt es sich im Konfigurationmenü eine niedrigere Auflösung bzw. eine niedrigere Monitorfrequenz einzustellen. <br>
            <span class="Stil1">A:</span> Eine weitere Möglichkeit sind die Einstellungen des Grafikkartentreibers: <br>
  sowohl Detonator Treiber (nVidia) als auch Catalyst Treiber (ATI) bieten den Benutzer die Möglichkeit der Leistungseinstellungen.<br>
  Bei Problemen empfehlen wir Ihnen die Wiedergabequalität auf niedrig bzw. auf "Leistungsoptimiert" zu stellen.<br>
  Antialiasing-Optionen, Anisotropische Filterung oder Texturschärfung belasten die Grafikkarte, führen zum Teil zu Darstellungsfehlern und sollten nur eingestellt werden, wenn die Komplett-performance Ihres Rechners dies zulässt. <br>
  Wenn keine der Einstellungen den gewünschten Erfolg gebracht hat, hilft auch hier vielleicht ein Treiberupdate. Unter http://www.treiber.de finden Sie eine Liste aller Hardwarehersteller. Auf den Webseiten der einzelnen Hersteller stehen i.d.R. aktuelle Treiber zum kostenlosen Download bereit. <br>
  <u>HINWEIS</u>: Sollten Sie Ihren Grafikkartentreiber updaten, ist es zwingend erforderlich DirectX 9.0c erneut zu installieren. <br>
          </p>
          <p align="justify"><u><strong>TIPPS ZU SOUNDPROBLEMEN </strong></u><br>
            <span class="Stil1">Q:</span> Wenn ich Earth 2160 starte, habe ich keine Soundausgabe. Was ist zu tun?<br>
            <span class="style1"><strong>A:</strong></span> Klicken Sie auf Start -&gt; Ausführen und geben Sie regedit ein. Klicken Sie auf OK. Wechseln Sie in den Zweig: HKEY_CURRENT_USER\Software\Reality Pump\Earth2160\Sound\ Doppelklicken Sie im rechten Fenster auf den Wert Enable3DSound. Ändern Sie den Wert von 1 auf 0 und verlassen Sie die Windows Registry. Starten Sie das Spiel erneut.            <br>
            <br>
            Wenn Ihnen obige FAQs nicht weitergeholfen haben, könnte Ihnen das Diagnoseprogramm weiterhelfen. <br>
            Klicken Sie hierfür auf Start - Ausführen und geben Sie als Befehlszeile DXDIAG ein. <br>
            Klicken nun auf den Karteikasten Sound. <br>
            Standardmäßig ist die Sound Hardwarebeschleunigung auf 100% eingestellt.<br>
            Stellen Sie probeweise den Schieberegler auf vorletzte Stufe (Standardbeschleunigung). <br>
            Starten Sie nun das Spiel erneut. Sollte es dennoch zu Soundausgabeproblemen kommen, rufen Sie erneut DXDIAG auf und stellen Sie den Schieberegler auf "Minimalbeschleunigung". <br>
            Sollte Dir auch diese Option nicht den gewünschten Erfolg bringen, schalten Sie die Soundbeschleunigung komplett ab. Stellen hierzu den Schieberegler auf "Keine Beschleunigung".<br>
            Wenn auch diese letzte Möglichkeit nicht den gewünschten Erfolg gebracht hat, hilft vielleicht ein Treiberupdate. <br>
            Unter http://www.treiber.de finden Sie eine Liste aller Hardwarehersteller.<br>
            Auf den Webseiten der einzelnen Hersteller stehen i.d.R. aktuelle Treiber zum kostenlosen Download bereit. <br>
            Vergleichen Sie nun die Dateiversion Ihrer Treiber mit der Treiberversion, die Sie auf der Webseite Ihres Hardware-Herstellers finden. Installieren Sie ggfs. den neueren Treiber für Ihre Hardware. <br>
          </p>
          <p align="justify"><u><strong>TIPPS ZU WINDOWS XP</strong></u><br>
            <span class="Stil1">Q:</span> Auf einigen Systemen kann es vorkommen, dass Windows XP beim DVD-Rom Wechsel nach der Installation, vor dem Spielstart (wenn der Earth-Datenträger gerade entfernt wurde) eine Fehlermeldung ausgibt. <br>
            <span class="Stil1">A:</span> Diese Fehlermeldung ist auf die "Automatische Benachrichtigung beim Wechsel" zurückzuführen, die bei Windows XP nicht ohne Eingriffe in die Registry abschaltbar ist. Klicken Sie in diesem Fall einfach so lange auf "Weiter", bis die Fehlermeldung verschwindet.<br>
          </p>
          <p align="justify"><u><strong>HOTLINE UND SUPPORT </strong></u><br>
              Wir haben das Produkt sehr vielen und ausführlichen Tests auf vielen unterschiedlichen Hardwarekonfigurationen unterzogen, so dass in der Regel keine Schwierigkeiten auftreten sollten. Dennoch ist es nicht möglich alle Konfigurationen auszutesten.<br>
              Bevor Sie bei unserem technischen Support anrufen, legen Sie sich bitte folgende Details zu Ihrer Hardware zurecht:<br>
              <br>
              Welches Betriebssystem benutzen Sie? <br>
              Was für einen Prozessor besitzen Sie?<br>
              Mit wie viel Hauptspeicher (RAM) ist Ihr Rechner ausgestattet? <br>
              Welche DirectX Version ist auf Ihrem Rechner installiert? <br>
              Was für eine Grafikkarte und Soundkarte ist in Ihrem Rechner eingebaut?<br>
              Was für eine Treiberversion der Grafik- bzw. Soundkarte benutzen Sie? </p>

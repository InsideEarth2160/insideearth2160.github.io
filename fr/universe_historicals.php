</em>EARTH 2160 est le tout nouvel épisode de la série EARTH et le digne successeur d' Earth 2140 et&nbsp <a href="http://www.earth2150.com" target="_blank">Earth 2150</a>.<br><br>
EARTH 2160, #1 des charts dès sa parution, apporte de nombreuses améliorations à cette série déjà exceptionnelle
 et fait l'objet d'appréciations élogieuses unanimes de la presse spécialisée.<br><br>
Seuls quelque milliers d'humains ont réussi à quitter la Terre avant que celle-ci ne soit détruite en 2150 apJC.
 Ils ont passé les quelques années suivantes à organiser la vie sur la planète rouge,
 par le développement de nouvelles technologies et la création de nouvelles bases de civilisation.<br><br>
On ne sait trop pourquoi, les factions: "Dynastie Eurasienne" et "Corporation  Lunaire",
 ont travaillé indépendamment l'une de l'autre et évolué séparément. Quant aux "Etats Civilisés Unis", seuls
 quelques restes disséminés dans le système solaire sont connus à ce jour.<br><br>
Mais, en l'an 2160, un événement majeur s'est produit dans ce nouveau monde...<br><br>
Au choix du joueur la DE, la CL et les ECU pourront, soit joindre leurs forces, soit poursuivre leurs sempiternels conflits.<br><br>
En plus des environnements habituels du système solaire, on découvrira aussi des endroits étranges:
 des planètes avec des surfaces liquides en mouvement et qui présentent d'étranges phénomènes de réflection.<br><br>
De plus, au-delà des schémas habituels des RTS (Real Time Strategy - Stratégie en Temps Réel) - acquisition
 de ressources, construction de bases et destruction des installations de l'ennemi, 
 Earth 2160 comporte aussi des éléments de RPG (Role Playing Game - Jeu de Rôle) en permettant à chaque joueur de s'identifier
 à l'un ou l'autre des héros et de poursuivre leurs quêtes.


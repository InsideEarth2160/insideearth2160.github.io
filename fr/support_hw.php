          <p align="left" class="h1 style1" style="margin-left:10px; margin-right:10px;"><strong> </strong></p>          
          <p><strong> <u>QUESTIONS RELATIVES AUX PROBLEMES DE MATERIEL </u></strong></p>
          <p> <span class="Stil2"><strong>Q: </strong></span>quand je joue &agrave; Earth 2160, l&rsquo;image n&rsquo;est pas fluide. J&rsquo;ai une GeForce FX5xxx. Que puis-je faire ? <br>
            <span class="Stil2"><strong>R: </strong></span>pour le cas des cartes graphiques dot&eacute;es d&rsquo;une puce nVidia GeForce FX5xxx , le Shader 2.0 est tr&egrave;s lent. Earth 2160 est n&eacute;anmoins compatible avec ces cartes, m&ecirc;me en utilisant le Shader 1.3. T&eacute;l&eacute;chargez la <a href="UseShaderVersion1.3.zip" target="_blank"><strong>cl&eacute; d&rsquo;enregistrement</strong></a> dans la base de registre, d&eacute;compressez-la et lancez-la. Earth 2160 devrait alors mieux fonctionner. </p>
          <p> <span class="Stil2"><strong>Q: </strong></span>j&rsquo;ai une carte m&egrave;re K7S5A. Earth 2160 plante juste apr&egrave;s le lancement du jeu, ce qui me ram&egrave;ne sur le Bureau&hellip; Pourquoi ? <br>
            <span class="Stil2"><strong>R: </strong></span>ce probl&egrave;me peut &ecirc;tre r&eacute;solu par une mise &agrave; jour du Bios. Pour cela, suivez <a href="http://www.biosflash.com/bios-update-anleitung.htm" target="_blank"><strong>les instructions</strong></a> ci-dessous. Les fichiers dont vous avez <a href="http://www.lejabeach.com/ECS/ez.html" target="_blank"><strong>besoin</strong></a> (la mise &agrave; jour 03/08/11 [030811.zip] et le programme amiflash [aminf341.zip]) sont sur la premi&egrave;re ligne de la liste. <br>
            INFORMATION : si vous n&rsquo;arrivez pas &agrave; r&eacute;soudre votre probl&egrave;me avec le bios ci-dessus, essayez <a href="K7S5A900.rar"><strong>celui-ci</strong></a> ! Ce BIOS a r&eacute;solu le probl&egrave;me de la plupart de nos utilisateurs. <br>
            ATTENTION ! Une mise &agrave; jour incorrecte du BIOS peut endommager votre carte m&egrave;re ! </p>
          <p> <span class="Stil2"><strong>Q: </strong></span>j&rsquo;ai un routeur et/ou un pare-feu, et je rencontre des probl&egrave;mes de connexion quand je veux jouer en mode multijoueur. Que puis-je faire ? <br>
            <span class="Stil2"><strong>R: </strong></span>votre routeur et/ou votre pare-feu contr&ocirc;le(nt) vos connexions : vous devez suivre ces instructions pour r&eacute;gler le probl&egrave;me. <br>
            Vous devez ouvrir un port UDP en entr&eacute;e pour jouer &agrave; Earth 2160. En principe, vous pouvez ouvrir n&rsquo;importe quel port qui n&rsquo;est pas d&eacute;j&agrave; utilis&eacute; par un autre utilisateur sur le routeur. Nous conseillons le Port 17771. Activez ce port sur votre pare-feu (ce qui permet les connexions entrantes et sortantes !) ou, si vous utilisez un pare-feu qui g&egrave;re les r&egrave;gles de connexion application par application, vous devez autoriser Earth 2160 &agrave; se connecter &agrave; Internet. Si vous utilisez un pare-feu Windows XP, Earth 2160 entrera directement dans la liste des exceptions (autoris&eacute;es). <br>
            Dans votre routeur, envoyez le port choisi vers votre ordinateur (pour cela, vous trouverez les options sous des noms comme &ldquo;Envoyer&rdquo; ou DNAT). Entrez le port et l&rsquo;IP de votre ordinateur sur le r&eacute;seau local. Si vous pouvez s&eacute;lectionner un fichier journal, choisissez plut&ocirc;t UDP ou Datagram. <br>
            Lancez Earth 2160, ouvrez le menu des r&eacute;glages via la page R&eacute;seau et cochez la case &ldquo;Port client&rdquo;. Entrez votre port, 17771 par exemple. Si vous avez un routeur et que vous voulez installer un jeu sur votre ordinateur, vous devez aussi cocher la case 'R&eacute;solution NAT&rsquo;. Ne modifiez pas les valeurs dans le champ de texte qui suit. <br>
            Si vous suivez ces &eacute;tapes, vous devriez pouvoir jouer en multijoueur sans probl&egrave;me (techniquement parlant en tout cas). Lancez une partie et demandez &agrave; quelqu&rsquo;un de vous rejoindre : s&rsquo;il y parvient, tout est normal. </p>
          <p><u>En cas de probl&egrave;mes : </u><br>
              Si personne n&rsquo;arrive &agrave; rejoindre votre partie alors que vous avez suivi la proc&eacute;dure recommand&eacute;e, essayez de lancer une 'R&eacute;solution NAT&rsquo;. Si cela ne fonctionne toujours pas, essayez d&rsquo;activer le port UDP 17172 dans votre pare-feu ou transf&eacute;rez les donn&eacute;es transitant par ce port du routeur vers votre ordinateur. <br>
              Nous n&rsquo;avons pas r&eacute;solu tous les probl&egrave;mes li&eacute;s &agrave; la connexion &agrave; une partie. Il se peut donc que vous puissiez parfois rejoindre une partie, mais que ce soit impossible &agrave; d&rsquo;autres moments, sans que vous y soyez pour rien : c&rsquo;est un probl&egrave;me dans le jeu. Il est important que tous les joueurs effectuent correctement leurs r&eacute;glages, comme indiqu&eacute; dans le manuel et cette FAQ. Et croyez-nous ! Nous essayons de trouver les bonnes solutions pour r&eacute;gler ce genre de probl&egrave;mes ! </p>
          <p>Amusez-vous bien ! </p>

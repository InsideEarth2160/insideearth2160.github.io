        <TD WIDTH=459 valign="top" bgcolor="#010102" style="background-image:url(images/index_06.gif); background-repeat:no-repeat;" ALT=""><p align="justify" style="margin-left:10px; margin-right:10px;"><strong class="h1"> Innovations </strong><em><br>
                  <br>
            </em><strong> Agents Virtuels:</strong><br><br> 
										Une dimension toute nouvelle a été ajoutée au jeu par l'introduction d'Agents Virtuels.
										Ces agents sont des sortes de mercenaires à engager qui vous aideront au cours de la partie. <br>
            <br>
    Chaque agent a des capacités propres (ils peuvent assister vos chercheurs, commander des unités, accroître l'efficacité
		de récolte des ressources, espionner l'ennemi, voire même corrompre les agents de l'ennemi).<br>
    <br>
    Les agents peuvent être engagés et voir leur contrat résilié tout au long de la partie.
		L'engagement des agents, dans une partie multi-joueurs, fait l'objet d'enchères au cours desquelles différents joueurs
		peuvent surenchérir, ce qui ajoute une dimension économique et offre la possibilité de défaire l'ennemi en ne lui
		permettant pas l'engagemet des agents, pour cause de manque de ressources.<br>
    <br>
    Chaque agent se remémore ses contrats précédents aves chaque joueur. L'historique de ces contrats à une influence
		importante sur le coût d'un agent. Chaque joueur doit prendre le plus grand soin des agents qu'il engage, faute de quoi
		ceux-ci pourraient ne plus vouloir travailler pour lui ou exiger un prix exorbitant par la suite. <br>
    <br>
    Les agents ont aussi leur propre appréciation respective des autres agents. Ils peuvent apprécier, aimer, détester,
		voire haïr d'autres agents. Ces relations peuvent aussi influer sur le prix du contrat passé avec un agent. Par exemple,
		il est possible qu'un agent se mette au service d'un joueur pour "deux fois rien" parce qu'un agent qu'il déteste et
		au service d'un opposant. <br>
    <br>
    Tous les agents ne sont pas disponibles à tout moment. Si un agent est sous contrat avec l'un ou l'autre des joueurs,
		il ou elle ne sera pas disponible pour les autres joueurs.<br>
    <br>
    <strong>Contruction modulaire: <br><br></strong>
		La plupart des bâtiments et unités permettent une construction modulaire. Chaque joueur peut construire ses bases et unités au départ de composants propres, ce qui offre une diversité très large.<br><br>
		Cette approche permet notamment à chaque joueur de créer sa propre armée, parfaitement adaptée à ses besoins stratégiques.<br>
    <br>
    Les structures qui composent les bases permettent elles aussi d'adapter leur composition au terrain ainsi qu'à l'objectif
		en cours d'accomplissement. Lors de la construction d'une tour de la CL, par exemple, la disposition (empilement) des modules
		peut avoir une importance primordiale sur son efficacité au combat. <br>
    <br>
    <strong>Recherches libres:</strong><br>
    <br>
    Chaque joueur peut entreprendre de multiples recherches simultanément, pour autant qu'il dispose de ressources suffisantes
		(nombre de centres de recherches, ressources et énergie disponibles).
		Certaines parties de l'Arborescence des Recherches ne sont disponibles pour un joueur que si les recherches dont dépend
		une technologie donnée est soit disponible ou à tout le moins débutée. 
		La technologie de base peut être un objet Alien, achetée à un allié ou encore dérobée à un ennemi. </p>

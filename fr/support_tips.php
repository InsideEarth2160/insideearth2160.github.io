        <TD WIDTH=459 valign="top" bgcolor="#010102" style="background-image:url(images/index_06.gif); background-repeat:no-repeat;" ALT=""><p align="left" style="margin-left:10px; margin-right:10px;"><strong class="h1"> ASTUCES de JEU</strong></p>
          <p align="left" class="h1 style1" style="margin-left:10px; margin-right:10px;"><strong> </strong></p>          
          <p><strong> <span class="style2">Q: </span></strong>Par quelle campagne commencer ? <br>
          <span class="style2"><strong>R: </strong></span>Il est pr&eacute;f&eacute;rable de commencer par la campagne ED, puisqu&rsquo;elle comprend un didacticiel complet pr&eacute;sentant l&rsquo;essentiel des mouvements de cam&eacute;ra et du contr&ocirc;le des unit&eacute;s. </p>
          <p><strong> <span class="style2">Q: </span></strong>Un &eacute;clair clignotant appara&icirc;t au-dessus de certains b&acirc;timents. Qu&rsquo;est-ce que cela signifie ? <br>
          <span class="style2"><strong>R: </strong></span>Cela signifie que vous manquez d&rsquo;&eacute;nergie, et sans suffisamment d&rsquo;&eacute;nergie, rien ne marchera. Soit votre centrale est hors de port&eacute;e (UCS), auquel cas vous devez construire un Transmetteur, soit il vous faut une centrale suppl&eacute;mentaire (LD, ED, UCS). </p>
          <p><strong><span class="style2">Q: </span></strong>Je veux que mes troupes patrouillent juste devant ma base. Comment puis-je faire ? <br>
            <span class="style2"><strong>R: </strong></span>S&eacute;lectionnez l&rsquo;unit&eacute; et appuyez sur la touche &ldquo;R&rdquo;, cliquez sur les diff&eacute;rents points de l&rsquo;itin&eacute;raire que vous souhaitez qu&rsquo;ils empruntent, et terminez l&rsquo;enregistrement en appuyant sur la touche &ldquo;X&rdquo;. Il y a malheureusement une limite au nombre de points que vous pouvez placer pour d&eacute;finir l&rsquo;itin&eacute;raire : seulement 65 512 ! ;) </p>
          <p><strong> <span class="style2">Q: </span></strong>Les vaisseaux extra-terrestres me rendent dingues. Comment puis-je les abattre ? <br>
          <span class="style2"><strong>R: </strong></span>Vous devez tr&egrave;s rapidement lancer des recherches sur les Missiles de D&eacute;fense Antia&eacute;rienne, car il est tr&egrave;s difficile de se d&eacute;barrasser des extra-terrestres uniquement avec des armes &eacute;nerg&eacute;tiques. </p>
          <p><strong> <span class="style2">Q: </span></strong>J&rsquo;ai envoy&eacute; mon h&eacute;ros dans un b&acirc;timent et je ne le retrouve plus. O&ugrave; est-il pass&eacute; ?<br>
          <span class="style2"><strong>R: </strong></span>Cliquez sur le portrait du H&eacute;ros situ&eacute; &agrave; gauche de l&rsquo;&eacute;cran. <br>
  S&eacute;lectionnez la zone o&ugrave; il se trouve actuellement, puis donnez l&rsquo;ordre de quitter le b&acirc;timent. </p>
          <p><strong> <span class="style2">Q: </span></strong>Le contr&ocirc;le &agrave; la souris ne me pla&icirc;t pas. J&rsquo;ai l&rsquo;habitude de donner des ordres avec le bouton droit de la souris. Est-il possible de changer ? <br>
          <span class="style2"><strong>R: </strong></span>Vous pouvez modifier les contr&ocirc;les de la souris dans les options de configuration des contr&ocirc;les. Vous devriez pouvoir trouver les r&eacute;glages qui vous conviennent. </p>
          <p><strong> <span class="style2">Q: </span></strong>La rotation 3D est trop sensible : j&rsquo;ai du mal &agrave; garder &agrave; l&rsquo;esprit la situation d&rsquo;ensemble. Est-ce que je peux la d&eacute;sactiver ? <br>
          <span class="style2"><strong>R: </strong></span>Dans les options de contr&ocirc;les, vous pouvez modifier l&rsquo;option de verrouillage de la cam&eacute;ra. Le mode de zoom automatique sera peut-&ecirc;tre plus &agrave; votre go&ucirc;t. </p>
          <p><strong><span class="style2">Q: </span></strong>Je ne vois rien et l&rsquo;&eacute;cran est rempli de parasites noirs et blancs. Comment puis-je r&eacute;gler &ccedil;a ? <br>
            <span class="style2"><strong>R: </strong></span>Vous &ecirc;tes dans une zone couverte par le Brouillard de Guerre : vous ne pouvez pas voir les territoires inexplor&eacute;s. Cliquez sur la minicarte &agrave; un endroit qui n&rsquo;est pas masqu&eacute; (votre base par exemple), et les choses devraient vous sembler plus normales. </p>
          <p><strong> <span class="style2">Q: </span></strong>(ED) Je veux construire une porte, mais j&rsquo;ai d&eacute;j&agrave; un mur &agrave; cet endroit. Comment faire ? <br>
          <span class="style2"><strong>R: </strong></span>Vendez les parties du mur que vous voulez remplacer. </p>
          <p><strong> <span class="style2">Q: </span></strong>(ED/UCS) Mes moissonneuses sont toutes d&eacute;truites et je suis &agrave; court de ressources. Comment puis-je continuer ? <br>
          <span class="style2"><strong>R: </strong></span>Pour obtenir les ressources n&eacute;cessaires &agrave; la construction d&rsquo;une moissonneuse sans avoir &agrave; les collecter, vous pouvez vendre un ou plusieurs b&acirc;timents (mais n&rsquo;oubliez pas d&rsquo;annuler tous les ordres de recherche et de production en cours, sinon vos ressources dispara&icirc;tront aussi vite qu&rsquo;elles sont apparues). </p>
          <p><strong> <span class="style2">Q: </span></strong>Je me fais tuer en mode multijoueur sur le serveur EarthNet : en quelques secondes, ils attaquent et je perds. Que dois-je faire pour y arriver ?<br>
          <span class="style2"><strong>R: </strong></span>Vous devriez tout d&rsquo;abord jouer aux campagnes afin d&rsquo;apprendre les diff&eacute;rentes m&eacute;thodes qui permettent de lancer des recherches et de construire des bases. Il est &eacute;galement souhaitable de jouer contre l&rsquo;ordinateur en mode Escarmouche avant d&rsquo;essayer d&rsquo;affronter les v&eacute;t&eacute;rans endurcis en multijoueur. </p>
          <p><strong> <span class="style2">Q: </span></strong>Comment faire des captures d&rsquo;&eacute;cran ? <br>
          <span class="style2"><strong>R: </strong></span>Au cours de la partie, appuyez sur la touche Impr &eacute;cran. L&rsquo;image sera sauvegard&eacute;e au format BMP dans le r&eacute;pertoire Mes documents/Earth 2160. Appuyez sur la touche Pause pour arr&ecirc;ter le jeu, puis r&eacute;glez la cam&eacute;ra, et enfin capturez l&rsquo;&eacute;cran. Si une unit&eacute; est s&eacute;lectionn&eacute;e, vous pouvez appuyer sur la touche TAB pour avancer son animation de 3 images (si la pose ne vous pla&icirc;t pas). </p>
          <p><strong> <span class="style2">Q: </span></strong>Mon &eacute;cran se fige pendant quelques secondes toutes les 5 minutes. Est-ce normal ? <br>
          <span class="style2"><strong>R: </strong></span>Cela est provoqu&eacute; par la fonction Sauvegarde automatique, qui se lance toutes les 5 minutes. Allez dans les options pour modifier le d&eacute;lai entre deux sauvegardes automatiques ou supprimer l&rsquo;option compl&egrave;tement. </p>
          <p><strong> <span class="style2">Q: </span></strong>Earth 2160 tourne tr&egrave;s lentement sur mon ordinateur. Faut-il que je diminue mes r&eacute;glages vid&eacute;o ? Comment puis-je am&eacute;liorer les performances de mon ordinateur ? <br>
          <span class="style2"><strong>R: </strong></span>Le moteur de Earth 2160 fait effectuer pratiquement tous les calculs graphiques au processeur de la carte graphique (et pas au processeur central). La vitesse du processeur principal n&rsquo;a donc pas d&rsquo;influence directe. Pour que votre PC soit mieux apte &agrave; faire fonctionner Earth 2160, changez votre carte graphique. Les mod&egrave;les ATI Radeon X700 sont d&rsquo;un tr&egrave;s bon rapport qualit&eacute;-prix. Bien s&ucirc;r, si vous voulez vous faire vraiment plaisir, achetez une carte graphique haut de gamme, qui vous offrira des performances &eacute;blouissantes. </p>
          <p><strong> <span class="style2">Q: </span></strong>J&rsquo;ai install&eacute; le jeu sur 2 ordinateurs, mais le mode multijoueur ne marche pas. Pourquoi ?<br>
          <span class="style2"><strong>R: </strong></span>Pour jouer en multijoueur, chaque copie install&eacute;e doit &ecirc;tre identifi&eacute;e par un num&eacute;ro de s&eacute;rie unique. Vous devez acheter un autre exemplaire du jeu ou un deuxi&egrave;me num&eacute;ro de s&eacute;rie. </p>

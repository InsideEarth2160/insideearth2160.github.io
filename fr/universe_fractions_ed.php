          </div>
          <p align="justify">Lors de la construction de vos bases, vous ne pourrez éviter d'avoir à édifier des bâtiments de Production et des structures de Défense.
								Selon la faction pour laquelle vous vous battez, vous disposerez pour cela de murs, de clôtures-laser,
								de barrages, de bunkers et de tours armées.
								Par la recherche de nouvelles technologies, vous disposerez d'armements up-to-date pour remplacer votre armement existant.
								Il ne vous faudra pas passer un temps très long, et de possibles oublis, à rechercher les armes à
								perfectionner. Cliquez simplement sur le bâtiment à perfectionner et ouvrez le panneau de Commande.
								Des boutons pour les perfectionnements disponibles apparaissent au bas du panneau.
								Cliquez sur le symbole voulu et votre nouvel armement sera installé.</p>

            <p><strong> <u>QUESTIONS ON INSTALLATION</u></strong></p>
            <p> <span class="Stil1">Q:</span> At installation start, a message appears: ...&quot;Setup.exe is not a valid Win32 application&quot;. What can I do? <br>
              <span class="Stil1">A:</span> When this error message is displayed, close all active programs running in the background. Press CTRL-ALT-DEL simultaneously to get a list of currently running programs. Now close each individual program, apart from SYSTRAY and Explorer. Start the installation again. </p>
            <p> <span class="Stil1">Q:</span> During the installation of Earth 2160, problems may occur at times. Installation isn't carried out properly. <br>
              <span class="Stil1">A:</span> If you have an anti-virus program installed, deactivate it before installation. Active virus scanners can negatively affect an installation procedure. </p>
            <p> <span class="Stil1">Q:</span> The installation program always stops when it reaches a certain percentage. Why? <br>
              <span class="Stil1">A:</span> There are three possible reasons for this: <br>
              <u>1.</u> You haven't enough storage space on your hard drive. Delete any unnecessary programs to free up more space - now re-install Earth 2160. <br>
              <u>2.</u> Files are being written to a damaged sector of your hard drive. If this is the case, run Scandisk, using the Intensive option. When Scandisk has done its work and displayed a message saying that the disk is OK, install Earth 2160 again. <br>
              <u>3.</u> Maybe the DVD is soiled by stains or fingerprints. Is the DVD dirty? If so, clean it with a soft, dry, clean, lint-free cloth, using inward-outward movements. </p>
            <p><strong> <u>QUESTIONS ON STARTING THE GAME</u> </strong></p>
            <p> <span class="Stil1">Q:</span> When Earth 2160 starts on Windows 2000 the DVD ROM isn't detected. What can I do? <br>
              <span class="Stil1">A:</span> Windows 2000 users need administrator rights to install and play the game properly. </p>
            <p> <span class="Stil1">Q:</span> At game start, the following error message appears - &quot;An error has occurred during initialization of Direct3D. This can be caused by your graphics card configuration or driver. Try to start the program in Safe Mode (- safe mode execution parameter).&quot; <br>
              <span class="Stil1">A:</span> Your graphics card does not support 1.2 or higher Per-Pixel Shader! Unfortunately, you need a new graphics card. </p>
            <p> <span class="Stil1">Q:</span> At game start, an error message appears - &quot;The file d3d9.dll can not be found&quot;. <br>
              <span class="Stil1">A:</span> Install DirectX, version 9.0c, and restart your computer. Your game should now function properly. </p>
            <p> <span class="Stil1">Q:</span> When I start the game I get the following error message - &quot;No 3D graphics card found&quot; or &quot;No D3D9 support found.&quot; What do I do? <br>
              <span class="Stil1">A:</span> Here as well, you are not using the latest graphic card driver and / or the latest version of DirectX 9.0. Update your driver and install at least the 9.0c version of Direct X. </p>
            <p> <span class="Stil1">Q:</span> When I start Earth 2160, I get this error message: &quot;The application Earth2160_SSE.exe (or Earth2160_SSE.exe) refers to a storage location with the address xxxx:xxxx, which cannot be read&quot;. <br>
              <span class="Stil1">A:</span> The graphics card driver you have installed is probably not compatible with Direct X. The only solution is to get a Direct X 9.0c - compatible driver from the manufacturer of your graphics card. </p>
            <p> <span class="Stil1">Q:</span> When I start Earth 2160, my cursor disappears and my computer locks up. <br>
              <span class="Stil1">A:</span> The sound card driver you have installed is probably not compatible with Direct X. The only solution is to get a Direct X 9.0c - compatible driver from the manufacturer of your sound card. </p>
            <p> <span class="Stil1">Q:</span> I can't hear any music or sound effects during the game, although the system hardware is properly connected. <br>
              <span class="Stil1">A:</span> Make sure that your sound card is compatible to Direct X 9.0c or higher. You should also contact the manufacturer of your sound card to get the most up-to-date driver. </p>
            <p> <span class="Stil1">Q:</span> The graphics are distorted. <br>
              <span class="Stil1">A:</span> You're probably using an older graphics card driver. Contact the manufacturer of your graphics card to get the most up-to-date driver. </p>
            <p> <span class="Stil1">Q:</span> Earth 2160 crashes without any error messages and returns to the Desktop. <br>
              <span class="Stil1">A:</span> This problem can have several causes. Here is a list of the most probable - <br>
            Make sure that your DVD is clean (check the reading surface for stains and scratches). <br>
            Make sure that the game's been properly installed. <br>
            Make sure that DirectX 9.0c has been properly installed. <br>
            Check that you have the most up-to-date driver for your graphics card and that it's Direct X 9.0c - compatible. <br>
            Check that you have the most up-to-date driver for your sound card and that it's Direct X 9.0c - compatible. <br>
            Check that the Virtual Memory option on your computer is activated. <br>
            Run Scandisk and Defragmentation. <br>
            Delete old temporary files (.TMP) in directory C:\WINDOWS\TEMP or C:\WINx\TEMP on your hard drive (using Windows Explorer) or run a data carrier cleaning process. <br>
            Don't forget to make sure that no anti-virus program like Norton's Anti-Virus is running in the background when you start Earth 2160. <br>
            Make sure that no Windows disk caching program is running in the background when you start Earth 2160. <br>
            Try de-installing the game and re-installing it. <br>
            Exit the game, reboot your computer and try again. <br>
            Please note: if your computer returns to the Desktop screen while you're playing Earth 2160, you should always restart the computer before you try playing again. Otherwise DirectDraw or DirectSound could lock up and the game won't be able to access your sound and graphics hardware. </p>
            <p> <span class="Stil1">Q:</span> When the game starts, I get this error message: &quot;No installed 3D graphics card located&quot; or &quot;no D3D9 support located. What can I do? <br>
              <span class="Stil1">A:</span> Once again - you're not using the most up-to-date graphics card driver and / or the most up-to-date Direct X 9.0 version. Please update the driver and then install Direct X - at least version 9.0c. </p>
            <p> <span class="Stil1">Q:</span> When I start Earth 2160, I see the Earth logo and then end up back at the Windows Desktop. How can I Start Earth? <strong><br>
                <span class="Stil1">A1:</span> </strong>This problem occurs when you have an older graphics card, such as GeForce 3 TI. To start the game, do the following: <br>
            Click on Start - Run and type in &quot;Regedit&quot;. <br>
            First save your existing Windows registry by clicking on File - Export. Choose a location and a file name. Click on the All button under the file name and select Save. <br>
            Now go to the tree: <br>
            HKEY_CURRENT_USER\Software\Reality Pump \Earth2160\Graphics <br>
            Double-click on Multi-sample Type and change the &quot;2&quot; to a &quot;0&quot;. <br>
            Exit the Windows registry and restart the game. <br>
            Alternatively, you can Start Earth 2160 in Safe mode by choosing this option in the Earth 2160 program Group in the Start Menu. <br>
            <span class="Stil1">A2:</span> This problem can be caused by Database Execution Prevention (DEP), which was installed with SP2. Here are the solutions for this: <br>
            <u>Suggestion 1:</u> <br>
            Condition - Service Pack 2 is installed!! <br>
            Variation a):<br>
            Deactivate the &quot;Data Execution Prevention&quot; in boot.ini <br>
            Click: Start - Control Panel - System - Advanced - Startup and Recovery Settings: Edit <br>
            Change the following: <br>
            multi(0)disk(0)rdisk(0)partition(1)\WINDOWS=&quot;Microsoft Windows XP Home Edition&quot; /fastdetect /execute <br>
            Save, Restart, you're done. <br>
            Variation b):<br>
            Leave the &quot;Data Execution Prevention&quot; for all programs enabled, apart from Earth.exe files <br>
            As in Variation a), open boot.ini and check that the last line this time is as follows: <br>
            multi(0)disk(0)rdisk(0)partition(1)\WINDOWS=&quot;Microsoft Windows XP Home Edition&quot; /fastdetect /noexecute=OptOut <br>
            If not: rewrite, save, press OK. <br>
            In the previous window, in &quot;Performance&quot;: Click Settings. <br>
            Now click the variation: Select &quot;Data execution prevention for all programs and services with the exception of the activated selections&quot; - now add all 6 Earth*.exe files from the game directory using &quot;Add&quot;. <br>
            Save, Restart, you're done. <br>
            <u>Suggestion 2:</u> <br>
            If the 1st suggestion doesn't work, you can try starting Earth 2160 in the window. <br>
            This isn't the most comfortable solution, but at least it will let you play until the developers have brought out a patch. <br>
            Start the game in Safe Mode. In settings, select a resolution (e.g. 800x600) and highlight the windows mode! <br>
            Exit the Safe Mode and start the game normally. Earth 2160 should now start in the window with the detail settings you entered in Safe Mode. <br>
            <strong> <span class="Stil1">A3:</span> </strong>Another cause for this type of problem can be the installation itself. It can happen that the installation routine copies the files incompletely. When you compare the installed files to the files on the DVD, you may find that some files are smaller than the DVD ones - just copy these files from the DVD manually into the relevant installation directory and overwrite the incomplete files. </p>
            <p><strong> <u>MULTIPROCESSOR SYSTEMS and HYPER-THREADING PROCESSORS</u></strong></p>
            <p><span class="Stil1">Q:</span> On systems with multiple processors or systems where several virtual processors are being simulated, the sound in the cutscenes may run asynchronously to the picture, or the hero's lines are prematurely cut off.<br>
              <strong> <span class="Stil1">A:</span> </strong>This happens because the game engine isn't getting a constant timing signal from the processor - other programs are running in the background. To correct this, Start Earth2160 with the following parameters: <br>
            Earth2160_SSE.EXE -cpu_UsePerformanceCounter - <br>
            This calculates the synchronization without using a CPU timing signal. <br>
            Earth2160_SSE.EXE -cpu_UseTickCount - <br>
            Uses the timing signal of the switching frequency energy source for synchronization. <br>
            Earth2160_SSE.EXE -cpu_UseOneProcessor - <br>
            The program is only run on a single processor. Careful! - if your processors are set at under 2 GHz, you may experience performance loss.</p>
            <p><u><strong>GRAPHICS</strong></u><strong></strong></p>
            <p> Call up the Direct X Diagnosis program. Click on the index card &quot;Display&quot;. In this option there are three test possibilities available: AGP Texture Acceleration, Direct3D acceleration and DirectDraw acceleration. Test all modes. Should an error arise through one of the options, you can deactivate it, but Direct3D Acceleration should <strong>not</strong> be deactivated, because then you will not be able to start the game. <br>
            If none of the above solutions solve your problems, a driver update may help. <br>
            You can find a list of all hardware manufacturers under http://www.treiber.de or under http://www.windrivers.com <br>
            <strong> NOTE:</strong> If you update your graphics card driver, it is absolutely necessary that you reinstall DirectX. </p>
            <p><em> </em><span class="Stil1">Q:</span> If your graphics card overheats and reboots your computer in the middle of a game, use a lower resolution in the Configuration Menu or a lower monitor frequency. <br>
            <strong> <span class="Stil1">A:</span> </strong>Another option is the graphics card driver settings: <br>
            Detonator Driver (nVidia) and Catalyst Driver (ATI) offer the user the option of performance settings. <br>
            If problems arise, we recommend that you set the playback quality to low or 'optimized for performance'. <br>
            Anti-aliasing options, anisotropic filtering and texture sharpening all put a strain on the graphics card and can at times lead to imaging errors - these three should only be set if the overall performance of your computer permits. <br>
            If none of the above solutions solve your problems, a driver update may help. <br>
            You can find a list of all hardware manufacturers under http://www.treiber.de or under http://www.windrivers.com <br>
            <strong> NOTE:</strong> If you update your graphics card driver, it is absolutely necessary that you reinstall DirectX. </p>
            <p><em> </em><u><strong>SOUND</strong></u></p>
            <p> <span class="Stil1">Q:</span> When starting Earth 2160, I do not hear the music and sound FX. What shall I do?<br>
              <span class="Stil1">A:</span> Click on Start -&gt; Run and type regedit. Confirm with OK. Open the key HKEY_CURRENT_USER\Software\Reality Pump\Earth2160\Sound\ and double-click on the right side of the window on the value Enable3DSound. Change the value from 1 to 0 and close the registry. Restart the game.<br>
              <br>
              Call up the Direct X Diagnosis program. Click on the index card sound. As default, the sound hardware acceleration is set to 100% (Maximum acceleration). Try setting the slide control to the penultimate setting (standard acceleration) and restart the game. Should sound problems arise call up DXDIAG again and set the slide control to Basic Acceleration. <br>
            Should this still be unsuccessful, turn off the sound acceleration completely and set the slide control to &quot;No Acceleration&quot;. <br>
            If this last option doesn't work either, a driver update may help. <br>
            At http://www.treiber.de you'll find a list of all hardware manufacturers. <br>
            You'll usually find current drivers to download free on individual manufacturers' websites. <br>
            Now compare the file version of your driver with the driver version on your hardware manufacturer's website. If necessary, install the newer driver for your hardware. </p>
            <p><strong> <u>TIPS FOR WINDOWS XP</u></strong></p>
            <p> On some Windows XP systems, a message will appear during a DVD-ROM change following the installation, but before the start of the game (when the Earth DVD is being removed). <br>
            This error message is caused by &quot;Automatic notification during change&quot;, which (in the case of Windows X)P can only be turned off by going into the registry. In this case, just click on &quot;Next&quot; until the error goes away. </p>

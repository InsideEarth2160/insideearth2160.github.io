        <TD WIDTH=459 valign="top" bgcolor="#010102" style="background-image:url(images/index_06.gif); background-repeat:no-repeat;" ALT=""><p align="left" style="margin-left:10px; margin-right:10px;"><strong class="h1"> GAME TIPS</strong></p>
          <p align="left" style="margin-left:10px; margin-right:10px;">&nbsp;</p>
          <p align="justify">            <strong><span class="Stil1">Q:</span> </strong>Which campaign should I start with? <strong><br>
            </strong><span class="Stil1">A:</span> You should start with the ED campaign, since it has a complete tutorial describing the basics of camera movement and unit control. </p>
          <p><strong> <span class="Stil1">Q:</span> </strong>A blinking lightning symbol appears over some buildings. What does it mean? <strong><br>
          </strong><span class="Stil1">A:</span> It means you're lacking power - and without enough power, nothing will work. Either your Power plant is out of range (UCS), in which case you should build a Transmitter, or you need an additional Power plant (LC, ED, UCS). </p>
          <p><strong> <span class="Stil1">Q:</span> </strong>I want my troops to patrol right in front of my base. How do I do that?<strong> <br>
          </strong><span class="Stil1">A:</span> Select the units and click &quot;R&quot; for Record, click the various spots which mark the route you want them to go and finish recording with &quot;X&quot; for Execute. Unfortunately, there is a limit to how many spots you can mark off to define your route: only 65.512 ! ;-) </p>
          <p><strong> <span class="Stil1">Q:</span></strong> The Alien ships are driving me crazy. How can I shoot them down?<strong> <br>
          </strong><span class="Stil1">A:</span> You really must research Air Defense Missiles at an early stage, since it's hard to get rid of the Aliens with energy weapons alone. </p>
          <p><strong> <span class="Stil1">Q:</span> </strong>I sent my hero into a building and now I can't find him anymore. Where is he?<strong> <br>
          </strong><span class="Stil1">A:</span> Click on the Hero picture at the left of the screen. <br>
          Select the building area where he's currently located. Now all you have to do is issue the command to exit the building. </p>
          <p><strong> <span class="Stil1">Q:</span> </strong>I don't like the mouse control. I'm accustomed to issuing orders with the right mouse button. Can I do that? <strong><br>
          </strong><span class="Stil1">A:</span> You can change your mouse button control under SETUP - CONTROL. Then everything will work the way you want it to. </p>
          <p><strong> <span class="Stil1">Q:</span> </strong>The whole 3D rotation is a bit much. I tend to lose sight of the overall picture. Can I turn it off? <strong><br>
          </strong><span class="Stil1">A:</span> Use SETUP - CONTROL - Locked Camera. There is another setting called &quot;Auto zoom&quot;, which might prove to be more to your liking. </p>
          <p><strong> <span class="Stil1">Q:</span> </strong>I'm all fogged out and the whole screen is flickering black and white. How do I fix this? <strong><br>
          </strong><span class="Stil1">A:</span> This is the so-called Fog of War, which hides unexplored territory. Click on the Minimap and you should be able to see things more clearly. </p>
          <p><strong> <span class="Stil1">Q:</span> </strong>(ED) I want to build a gate, but my wall is already there. How do I do it? <strong><br>
          </strong><span class="Stil1">A:</span> Sell the pieces of the wall that you want to replace. </p>
          <p><strong> <span class="Stil1">Q:</span> </strong>(ED/UCS) My harvesters are destroyed and I'm out of resources. How do I continue? <strong><br>
          </strong><span class="Stil1">A:</span> To get enough resources for a new harvester without collecting them, you can sell one or more buildings - but be sure to cancel any running research and production orders, otherwise the resources will be gone as quickly as they come in. </p>
          <p><strong> <span class="Stil1">Q:</span> </strong>I'm getting killed in Multiplayer on the EarthNet Server. After just a few seconds they attack and I lose. How can I learn the game better? <strong><br>
          </strong><span class="Stil1">A:</span> You should first play the campaigns in order to learn each side's method of conducting research and building bases. It's also a good idea to play against the computer in Skirmish mode before trying to fight the hardened veterans of the Server. </p>
          <p><strong><span class="Stil1">Q:</span> </strong>How do I make screen shots? <strong><br>
            </strong><span class="Stil1">A:</span> During the game, just press the Print key. The screens will be saved in BMP format in the My Documents folder, Sub-directory Earth 2160 files. <br>
            You should first use the &quot;Pause&quot; key to stop play, then set the camera position and capture the shot. When you've selected a unit, you can use the TAB key to jump 3 pictures forward. </p>
          <p><strong> <span class="Stil1">Q:</span> </strong>My screen freezes up for a few seconds every 5 minutes. Is that normal? <strong><br>
          </strong><span class="Stil1">A:</span> That's caused by the auto-save function, which is set to 5 minutes. Use SETTINGS to either set this to a longer interval or turn it off completely. </p>
          <p><strong> <span class="Stil1">Q:</span> </strong>Earth 2160 runs very slowly on my PC - or do I need to reduce the video settings? How can I better equip my PC? <strong><br>
          </strong><span class="Stil1">A:</span> The Earth 2160 engine carries out nearly all graphic calculations at the graphic card level, so the speed of the processor doesn't really matter. To make your PC more Earth 2160 - ready, replace your graphic card. The ATI Radeon X700 models have a very good price-performance ratio. Of course, if you really want to go to town on this, Earth 2160 will greatly appreciate any top-of-the-line card which has a high frame rate. </p>
          <p><strong> <span class="Stil1">Q:</span></strong> I've installed the game on 2 PCs, but the multiplayer doesn't work. Why? <strong><br>
          </strong><span class="Stil1">A:</span> For multiplayer support, each installation needs its own serial number. You need another game or a new serial number.</p>

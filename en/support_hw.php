          <p align="left" style="margin-left:10px; margin-right:10px;">&nbsp;</p>
          <p><strong> <u>QUESTIONS ON HARDWARE PROBLEMS </u></strong></p>
          <p> <strong><span class="Stil1">Q:</span> </strong>Earth 2160 runs only jerkily on my GeForce FX5xxx. What can I do?<br> 
            <span class="Stil1">A:</span> In the case of nVidia GeForce FX5xxx series graphics cards, the Shader model 2.0 is very slow. However, Earth 2160 can be played with these cards, also with Shader 1.3. Just download the <a href="UseShaderVersion1.3.zip"><strong>provided reg key</strong></a>, unzip it, then run it. Earth 2160 should now run better for you. </p>
          <p> <strong><span class="Stil1">Q:</span> </strong>I have a K7S5A mainboard and Earth 2160 crashes just after I start the game, returning to the Desktop display...why? <br>
            <span class="Stil1">A:</span> This problem can be solved with a Bios update. You'll find <a href="http://www.biosflash.com/bios-update-anleitung.htm" target="_blank"><strong>instructions on this here</strong></a>. <a href="http://www.lejabeach.com/ECS/ez.html" target="_blank"><strong>The files needed</strong></a> (the 03/08/11 update [030811.zip] and the amiflash program [ aminf341.zip]) are in the topmost line of the table. </p>
          <p> INFO: Whoever's had no success with the bios above, you could try it with <a href="K7S5A900.rar"><strong>this one!</strong></a> <br>
            According to many of our users, this BIOS solved the problem for them. <br>
            ATTENTION! A incorrectly performed Bios update can damage your mainboard! </p>
          <p> <strong><span class="Stil1">Q:</span> </strong>I sit behind a router and / or a firewall and have connection problems when I want to play Multiplayer mode. What can I do? <br>
            <span class="Stil1">A:</span> If you're behind a router and / or a firewall, that is (are) monitoring your connections, then you'll have to do a couple of things first. <br>
            Earth 2160 needs at least a UDP port, through which your PC is accessible. In principle, you could choose any one, as long as it doesn't intersect with other people sitting behind the same router - if that isn't the case, we recommend Port 17771. Activate this port in your firewall (permit incoming and outgoing connections!), or, if you're using a firewall that bases its rules on applications, then permit Earth 2160 to connect to the Internet. If you're using the Windows XP firewall, Earth 2160 will automatically be entered in the exceptions (permitted) list. <br>
            In your router, forward the selected port to your own computer (the options for this are mostly to be found under names like 'Forward' or DNAT). Enter the port and the IP of your computer in the local network. If you can choose a logfile, then use UDP or Datagram. <br>
            Now start Earth 2160, open the settings and via the Network page, activate the 'Custom Port' check mark&quot;. Now enter your selected port, e.g. 17771. <br>
            If you have a router and you want to host a game, it's usually necessary to activate the 'NAT Resolver' check mark as well. The settings in the neighboring text field should be at standard. <br>
            If you carry out these steps, Multiplayer battles on the Internet should now be no problem for you (we mean technically, not victory-wise J ). Try hosting a game and ask someone to join it. If this works, everything's ok. </p>
          <p> <u>Troubleshooting: </u><br>
            If nobody can join your game - in spite of your carrying out the above procedures, try switching on the 'NAT Resolver'. If it STILL doesn't work, try activating Port 17172 UDP in your firewall, or even forward this port from the router to your computer. <br>
            At present, there are a few teething problems as far as joining a game goes. So if joining a game sometimes works, the times when it fails have nothing to do with you - it's a game thing. Just remember that all the other players must make the correct settings for their routers and their firewalls in accordance with this manual - and don't worry - this problem will be tackled and dealt with! </p>
          <p> Have fun! </p>          <p align="justify">&nbsp;</p>

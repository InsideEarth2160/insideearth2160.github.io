        <TD WIDTH=459 valign="top" bgcolor="#010102" style="background-image:url(images/index_06.gif); background-repeat:no-repeat;" ALT=""><p align="left" style="margin-left:10px; margin-right:10px;"><strong class="h1">        <strong> FAQ's - ACTIVATION</strong>S </strong></p>
          <p align="left" style="margin-left:10px; margin-right:10px;">&nbsp;</p>
          <p align="justify"><u><strong><strong>QUESTIONS ON PRODUCT ACTIVATION</strong></strong></u> </p>
          <p> <span class="Stil1">Q:</span> In a few years, the support for Earth 2160 could be gone - this would make activation impossible - so how can I keep playing the game then?<br>
             
            <span class="Stil1">A:</span> If activation isn't possible from a technical standpoint, or for other reasons in a few years' time, we'll make a patch available for you all well before that time comes! </p>
          <p> <span class="Stil1">Q:</span> What data is transferred from my PC during online activation? <br>
            <span class="Stil1">A:</span> Only a 16-digit activation number is transferred - and a 16-digit activation switching code is sent back to your PC. This number contains parts of the serial number. </p>
          <p> <span class="Stil1">Q:</span> Can I rent Earth 2160 in video stores? <br>
            <span class="Stil1">A:</span> No. Fundamentally, commercial lending of the sales versions of our games is not permitted. If you want to rent Earth 2160, you'll be able to do this from August onwards (in Germany) at T-Online Games On Demand. </p>
          <p> <span class="Stil1">Q:</span> How often can I (or must I) activate one version on different computers (e.g. notebook and stand-alone PC)? <br>
            <span class="Stil1">A:</span> Without changing your hardware - as often as you want. With a hardware change - 3 times online, or by telephone without having to register. You'll need to register the 4th time. For further activations, which are basically possible, you'll need to contact ***Zuxxez*** personally. As long as it's the same user or the buyer of a second-hand version, an activation code will be provided. </p>
          <p> <span class="Stil1">Q:</span> What happens if I want to sell my game? Can the buyer register this same version again, or activate it again? <br>
            <span class="Stil1">A:</span> Yes. If 4 hardware changes have taken place, the new customer has to call the Hotline and explain that he or she has bought this particular version second-hand. The activation will be reset and the product can be activated again. In this case, it's better to keep your receipt/proof of purchase. </p>
          <p> <span class="Stil1">Q:</span> Does the game have to be reactivated with each new installation, or where can I re-enter the code I got in the confirmation mail? <br>
            <span class="Stil1">A:</span> You can activate the game as often as you want, as long as you have the same hardware configuration. Or can I re-enter the code I already have (because it doesn't change)? Select &quot;Activate by Telephone&quot; - but don't make a call - just enter the code you already have. </p>
          <p> <span class="Stil1">Q:</span> How often can I activate Earth 2160 in one day? <br>
            <span class="Stil1">A:</span> As often as you want - as long as the hardware stays the same. </p>
          <p> <span class="Stil1">Q:</span> I often re-install my operating system. Do I have to activate each time I do this? <br>
            <span class="Stil1">A:</span> Yes, after every new installation you must re-activate the game - but your old activation code will work here too - if the hardware hasn't changed. Of course you can simply re-activate via the Internet each time as well. That's much easier than looking for the code and painstakingly entering it. </p>
          <p> <span class="Stil1">Q:</span> During telephone activation, do I have to provide my personal data - and if so, what kind of personal data? <br>
            <span class="Stil1">A:</span> No. You'll only have to register if there's been a 3x hardware change (which negates activation, of course). </p>
          <p> <span class="Stil1">Q:</span> In Multiplayer mode, does each player need an individual full version with its own code? <br>
            <span class="Stil1">A:</span> Yes. Each version playing simultaneously in a MP game must have its own, different serial number. </p>
          <p> <span class="Stil1">Q:</span> What happens if I lend my game to a friend and he activates this version? <br>
            <span class="Stil1">A:</span> Then the active version will be located on your pal's PC. If you want to re-activate your game on your own PC at a later point in time, just give us a call and explain the situation. </p>
          <p> <span class="Stil1">Q:</span> May I also install and activate Earth 2160 on my notebook? <br>
            <span class="Stil1">A:</span> As long as you don't use both games at once, we don't mind. </p>
          <p> <span class="Stil1">Q:</span> May I make a back-up copy of Earth 2160? <br>
            <span class="Stil1">A:</span> Yes. If you don't let all your friends and relatives have it, why not - but it has to be just that - a back-up copy, only for emergencies! </p>

We dont know what weird interaction is happening but this is 100% replicable.</p>
            <p><span class="Stil1">Q: </span>When you launch the game from steam and get the message:<br>
              ============  <br>
              Earth 2160 Error!<br>
              There was an error authorizing your Earth2160 install.<br>
              Please go to:<br>
              http://support.earth2160.com/<br>
              for help solving this problem.<br>
              ============<br>
              What can i do?
              <br>
              <span class="Stil1">A:</span> This message means that you have:<br>
              1. a invalid steam ticket.<br>
              In this case log your steam account out, restart the steam client and login again. if this doesn't help contact the <a href="http://support.steampowered.com/cgi-bin/steampowered.cfg/php/enduser/std_alp.php" target="_blank">Steam support </a> <br>
              or <br>
              2. your network connection is down/unworkable.<br>
              In this case contact your system administrator ;-) <br>
              or<br>
              3. you cannot connect to backend server.<br>
              In this case check that the ports 6668 TCP/IP  and ports 27015, 27031, 27039 UDP are configered right on your firewall/router. </p>
            <p><span class="Stil1">Q:</span> If you get the message above with the 
              Error code:<br>
              WinSock exception (Timeout while trying to connect to back-end server)<br>
              How to fix it?
              <br>
              <span class="Stil1">A:</span> If you have an anti-virus program installed, deactivate it. Active virus scanners can negatively affect an installation procedure.<br>
              If this doesn't help try this <a href="http://support.steampowered.com/cgi-bin/steampowered.cfg/php/enduser/std_adp.php?p_faqid=215&p_created=1094271654&p_sid=2R5nIu5i&p_lva=&p_sp=cF9zcmNoPSZwX3NvcnRfYnk9JnBfZ3JpZHNvcnQ9JnBfcm93X2NudD0yNzQmcF9wcm9kcz0mcF9jYXRzPSZwX3B2PSZwX2N2PSZwX3BhZ2U9NA**&p_li=&p_topview=1" target="_blank">Winsock fix for XP</a>. </p>
